package LeetCode.Easy;

import java.util.Scanner;

/*

https://leetcode.com/problems/find-the-difference/

You are given two strings s and t.

String t is generated by random shuffling string s and then add one more letter at a random position.

Return the letter that was added to t.

 

Example 1:

Input: s = "abcd", t = "abcde"
Output: "e"
Explanation: 'e' is the letter that was added.
Example 2:

Input: s = "", t = "y"
Output: "y"
 

Constraints:

0 <= s.length <= 1000
t.length == s.length + 1
s and t consist of lowercase English letters.

*/

public class threehundredandeightynine {

	public char findTheDifference(String s, String t) {
		int ans = 0;
		String c = s+t;
		for(char c_char: c.toCharArray()) ans ^= (int)(c_char - 'a');
		return (char)(ans + 'a');
	}

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.print("Enter string s: ");
		String s = sc.next();
		System.out.print("Enter string t: ");
		String t = sc.next();
		threehundredandeightynine objThreehundredandeightynine = new threehundredandeightynine();
		System.out.println(objThreehundredandeightynine.findTheDifference(s, t));
		sc.close();
	}
	
}
